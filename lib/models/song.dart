import 'dart:convert';

class Song {
  final String id;
  final String title;
  final String titleHindi;
  final String artist;
  final String album;
  final String genre;
  final String region;
  final String duration;
  final String audioUrl;
  final String imageUrl;
  final List<String> tags;
  final DateTime? uploadedAt;

  const Song({
    required this.id,
    required this.title,
    required this.titleHindi,
    required this.artist,
    required this.album,
    required this.genre,
    required this.region,
    required this.duration,
    required this.audioUrl,
    required this.imageUrl,
    required this.tags,
    this.uploadedAt,
  });

  Map<String, dynamic> toJson() => {
        'id': id,
        'title': title,
        'titleHindi': titleHindi,
        'artist': artist,
        'album': album,
        'genre': genre,
        'region': region,
        'duration': duration,
        'audioUrl': audioUrl,
        'imageUrl': imageUrl,
        'tags': tags,
        'uploadedAt': uploadedAt?.toIso8601String(),
      };

  factory Song.fromJson(Map<String, dynamic> j) => Song(
        id: j['id'] as String,
        title: j['title'] as String,
        titleHindi: j['titleHindi'] as String,
        artist: j['artist'] as String,
        album: j['album'] as String? ?? '',
        genre: j['genre'] as String? ?? 'Folk',
        region: j['region'] as String? ?? 'Garhwal',
        duration: j['duration'] as String? ?? '0:00',
        audioUrl: j['audioUrl'] as String,
        imageUrl: j['imageUrl'] as String? ??
            'https://picsum.photos/seed/${j['id']}/400/400',
        tags: List<String>.from(j['tags'] as List? ?? []),
        uploadedAt: j['uploadedAt'] != null
            ? DateTime.parse(j['uploadedAt'] as String)
            : null,
      );

  String toJsonString() => jsonEncode(toJson());
}

